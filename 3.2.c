//计算存款利息。有1000元，想存一年。有3种方法可选：（1）活期，年利率为r1;(2)一年期定期，年利率为r2;(3)存两次半年定期，年利率为r3
//请分别计算出一年后按三种方法得到的本息和
#include<stdio.h>
//（1）本息和p1=(1+r1)*p0; (2)p2=p0*(1+r2); (3) p3=p0*(1+r3/2)*(1+r3/2);

int main()
{
    float p0=1000,r1=0.0036,r2=0.0025,r3=0.0198,p1,p2,p3;

    p1=(1+r1)*p0;
    p2=p0*(1+r2);
    p3=p0*(1+r3/2)*(1+r3/2);

    printf("p1=%f\np2=%f\np3=%f\n",p1,p2,p3);
    return 0;
}





